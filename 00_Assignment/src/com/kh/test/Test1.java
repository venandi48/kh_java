package com.kh.test;

public class Test1 {

	public static void main(String[] args) {
		
		//정수
		int num1 = 100;
		long num2 = 999_999_999_999L;
		System.out.println(num1);
		System.out.println(num2);
		
		//실수
		float dotNum1 = 486.567f;
		double dotNum2 = 567.890123;
		System.out.println(dotNum1);
		System.out.println(dotNum2);
		
		//문자
		char ch = 'A';
		System.out.println(ch);
		
		String str = "Hello World";
		System.out.println(str);
		
		boolean tf = true;
		System.out.println(tf);
		
	}
}